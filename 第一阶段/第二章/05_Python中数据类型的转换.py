# 将数字类型转换为字符串
num_str = str(1)
print(type(num_str), num_str)

float_str = str(13.14)
print(type(float_str), float_str)
# 将字符串转换为数字
str_num = int("222")
print(type(str_num), str_num)

str_float = float(13.14)
print(type(str_float), str_float)

float_num = int(13.14)
print(type(float_num), float_num)

num_float = float(13)
print(type(num_float), num_float)
